function actionSection(art) {
  const topic = art.h1
  return {
    h2: 'Your next hour checklist',
    paragraphs: [
      `Finish this guide on ${topic} with three written lines: who will play, where the instrument lives, and your real budget including case and shipping. Those lines filter listings faster than scrolling photos.`,
      `Listen to two recordings in the same scale while wearing headphones. Compare sustain after the attack fades, not just the first bright strike. Use the Stillforge player as one reference if you lack local instruments.`,
      `If your budget lands in mid artisan tier ($1,400-$2,500) or a Stillforge tank-forged commission near $1,800, send makers those three lines plus a request for tuning video policy. Skip deposits until answers match this guide.`,
    ],
  }
}

export function withDepth(articles) {
  return articles.map((art) => {
    const extra = depthBySlug[art.slug] || []
    const sections = [...art.sections, ...extra, actionSection(art)]
    const text = sections
      .flatMap((s) => [
        ...(s.paragraphs || []),
        ...(s.list || []),
        ...(s.table ? [s.table.headers.join(' '), ...s.table.rows.flat()] : []),
      ])
      .join(' ')
    const words = text.split(/\s+/).filter(Boolean).length
    const faqWords = art.faqs.flatMap((f) => [f.q, f.a]).join(' ').split(/\s+/).filter(Boolean).length
    const readingMinutes = Math.max(art.readingMinutes || 8, Math.ceil((words + faqWords) / 200))
    return { ...art, sections, readingMinutes }
  })
}
