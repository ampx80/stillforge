export function withDepth(articles) {
  return articles.map((art) => {
    const extra = depthBySlug[art.slug] || []
    const tail = longTailBySlug[art.slug]
    const sections = [
      ...art.sections,
      ...extra,
      actionSection(art),
      ...(tail ? [tail] : []),
      stillforgeContext(art),
    ]
    const text = sections
      .flatMap((s) => [
        ...(s.paragraphs || []),
        ...(s.list || []),
        ...(s.table ? [s.table.headers.join(' '), ...s.table.rows.flat()] : []),
      ])
      .join(' ')
    const words = text.split(/\s+/).filter(Boolean).length
    const faqWords = art.faqs.flatMap((f) => [f.q, f.a]).join(' ').split(/\s+/).filter(Boolean).length
    const readingMinutes = Math.max(art.readingMinutes || 8, Math.ceil((words + faqWords) / 200))
    return { ...art, sections, readingMinutes }
  })
}
